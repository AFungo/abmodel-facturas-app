    private void loadTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadTicketActionPerformed
        Map<String, String> values = new HashMap<>();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        //amounts
        values.put("amountImpEx", amountImpExTextField.getText());
        values.put("exchangeType", exchangeTypeTextField.getText());
        values.put("iva", ivaTextField.getText());
        values.put("ivaTax", ivaTaxTextField.getText());
        values.put("ivaTax1", ivaTax1TextField.getText());
        values.put("ivaTax2", ivaTax2TextField.getText());
        values.put("netAmountWI", netAmountWITextField.getText());
        values.put("netAmountWOI", netAmountWOITextField.getText());
        values.put("exchangeMoney", (String) exchangeMoneyComboBox.getSelectedItem());
        values.put("profits", profitsTextField.getText());
        values.put("totalAmount", totalAmountTextField.getText());
        
        //ticket data
        values.put("number", numberTextField.getText());
        values.put("type", (String) ticketType.getSelectedItem());
        values.put("issuedByMe", String.valueOf(issuedByMeCheckBox.isSelected()));        
        values.put("sector", (String) sectorsComboBox.getSelectedItem());
        values.put("delivered", String.valueOf(deliveredCheckBox.isSelected()));
        
        
        SQLFilter filter = new SQLFilter();
        filter.add("name", "=", providersComboBox.getSelectedItem(), String.class);
        if (docNoComboBox.isEnabled() && docNoComboBox.getSelectedItem() != null) {
            filter.add("docNo", "=", docNoComboBox.getSelectedItem(), String.class);
        }
        
        List<Provider> providersCheck = ProviderDAO.get(filter);

        String errorMessage = controller.validateParam(dateDateChooser.getDate(), values, true, 
                sectorsComboBox, providersCheck);
        if (errorMessage != null) {
            JOptionPane.showMessageDialog(this, errorMessage, "Los siguientes datos son invalidos", 
                    JOptionPane.ERROR_MESSAGE);
            return ;
        }
        
        //provider
        Provider provider = providersCheck.get(0);
        values.put("docNo", (String) provider.getValues().get("docNo"));
        values.put("docType", (String) provider.getValues().get("docType"));
        values.put("name", (String) provider.getValues().get("name"));
        values.put("provSector", (String) provider.getValues().get("provSector"));
        values.put("alias", (String) provider.getValues().get("alias"));

        values.put("date", sdf.format(dateDateChooser.getDate()));
        getIva(values);
        
        controller.loadTicket(values);
        cleanTextFields();
        docNoComboBox.setSelectedIndex(-1);//clean provider doc no
        updateLastTicketLoaded(values);
    }//GEN-LAST:event_loadTicketActionPerformed

    private void getIva(Map<String, String> values) {
        Float iva = 0.0f;
        if (!values.get("ivaTax").isEmpty())
            iva += Float.parseFloat(values.get("ivaTax"));
        if (!values.get("ivaTax1").isEmpty())
            iva += Float.parseFloat(values.get("ivaTax1"));
        if (!values.get("ivaTax2").isEmpty())
            iva += Float.parseFloat(values.get("ivaTax2"));
        
        values.put("ivaTax", iva.toString());
    }

    private void updateLastTicketLoaded(Map<String, String> values){
        showLastIvaTextField.setText(values.get("ivaTax"));
        showLastProviderTextField.setText(values.get("name"));
        showLastTicketNumberTextField.setText(values.get("number"));
        showLastTypeTextField.setText(values.get("type"));
        showLastTotalTextField.setText(values.get("totalAmount"));
        showLastDateTextField1.setText(values.get("date"));
        
        SQLFilter filter = FilterUtils.createTicketFilter(values);
        Ticket ticket = (Ticket)controller.getTickets(filter).get(0);
        showLastIDTextField1.setText(String.valueOf(ticket.getValues().get("id")));
    }
    
